// Helper function to get template by type and language
export function getPresellTemplate(type: string, language: string): PresellTemplate | null {
  const templates = presellTemplates[type as keyof typeof presellTemplates]
  if (!templates) return null
  
  const languageCode = language.toLowerCase().includes('port') ? 'pt' : 
                      language.toLowerCase().includes('ingl') || language.toLowerCase().includes('eng') ? 'en' : 
                      language.toLowerCase().includes('span') || language.toLowerCase().includes('esp') ? 'es' : 'pt'
  
  const template = templates[languageCode as keyof typeof templates] || templates.pt
  
  // Check if template has any properties - if not, return null for empty templates
  if (typeof template === 'object' && Object.keys(template).length === 0) {
    return null
  }
  
  return template as PresellTemplate
}